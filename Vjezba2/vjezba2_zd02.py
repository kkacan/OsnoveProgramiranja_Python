# unos dva decimalna broja

broj_1=float(input("Upiši prvi decimalni broj: "))
broj_2=float(input("Upiši drugi decimalni broj: "))

# izračun i ispis
print("Zbroj je",broj_1+broj_2)
print("Razlika je",broj_1-broj_2)
print("Umnožak je",broj_1*broj_2)
print("Kvocijent je",broj_1/broj_2)
print("Cjelobrojni kvocijent je",int(broj_1//broj_2))
print("Potencija je",broj_1**broj_2)
print("Ostatak pri cjelobrojnom dijeljenju je",int(broj_1%broj_2))



